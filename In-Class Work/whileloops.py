print("Example 1") # Example 1
x = 1
while x < 5:
    x = x + 1
print(x)
# 5 should print

print("Example 2") # Example 2
x = 1
while x < 5:
    print(x)
# 1 will repeatedly print forever

print("Example 3") # Example 3
x = 1
while x < 5:
    x = x - 1
print(x)
# Nothing will print - infinite loop
